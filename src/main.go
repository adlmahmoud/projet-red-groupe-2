func gameMenu(player *Character) {
	scanner := bufio.NewScanner(os.Stdin)

	for {
		color.Cyan("\n=== MENU PRINCIPAL ===")
		fmt.Println("1. Afficher les informations du personnage")
		fmt.Println("2. Accéder à l'inventaire")
		fmt.Println("3. Visiter le marchand")
		fmt.Println("4. Forgeron")
		fmt.Println("5. Combat d'entraînement")
		fmt.Println("6. Qui sont-ils?")
		fmt.Println("0. Quitter")

		fmt.Print("Votre choix: ")
		scanner.Scan()
		choice := scanner.Text()

		switch choice {
		case "1":
			player.DisplayInfo()
		case "2":
			AccessInventory(player)
		case "3":
			VisitShop(player)
		case "4":
			VisitBlacksmith(player)
		case "5":
			StartTrainingFight(player)
		case "6":
			whoAreThey()
		case "0":
			color.Green("Merci d'avoir joué à Ynov Kingdom!")
			return
		default:
			color.Red("Choix invalide. Veuillez choisir une option valide.")
		}
	}
}

